import type { FuelProduct } from "./truck-data"

export interface ProductBreakdown {
  product: FuelProduct
  volume: number
}

export interface ExtractionOrder {
  id: string
  customerId: string
  customerName: string
  shipToAddress: string
  latitude: number
  longitude: number
  status: "pending" | "assigned" | "in-progress" | "completed"
  volume: number // in gallons (total across all products)
  scheduledDate: string
  zoneId: string
  hubId: string
  driverId?: string
  city: string
  state: string
  zip: string
  tankSize: number
  currentLevel: number // percentage
  daysUntilEmpty: number
  priority: "High" | "Medium" | "Low"
  lastDelivery: string
  zone: string
  routeId?: string
  routeSequence?: number
  orderType?: "T" | "L" | "D" // Transfer, Load, Delivery
  productBreakdown?: ProductBreakdown[] // per-product volumes (optional — Route 6 doesn't use this)
}

// ShipTo - customer locations that may or may not have orders for a given day
export interface ShipTo {
  id: string
  customerId: string
  customerName: string
  shipToAddress: string
  latitude: number
  longitude: number
  city: string
  state: string
  zip: string
  tankSize: number
  lastDelivery: string
  zone: string
}

export interface Driver {
  id: string
  name: string
  vehicle: string
  status: "available" | "on-route" | "off-duty"
  currentLocation?: { lat: number; lng: number }
  hubId: string
}

export interface Zone {
  id: string
  name: string
  region: string
  latitude: number
  longitude: number
}

export interface Hub {
  id: string
  name: string
  erpId: string
  address: string
  latitude?: number
  longitude?: number
  zoneId: string
}

